export const UnicodeToGSM = (string) => {
    const stringArray = string.split('');

    for (let key in unicode) {
        const origin = unicode[key];
        const exist = stringArray.find(i => i === key);

        if (exist) {
            string = string.replaceAll(key, origin);
        }
    }

    return string;
};
